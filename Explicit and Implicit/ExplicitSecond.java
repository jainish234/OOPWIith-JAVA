class ExplicitSecond{
    public static void main(String[] args){
        long sq=12;
        System.out.println("long."+(sq));
        int sq1=(int)sq;
        System.out.println("int."+(sq1));
        short sq2=(short)sq1;
        System.out.println("short."+(sq2));
        byte sq3=(byte)sq2;
        System.out.println("byte."+(sq3));
    }
}