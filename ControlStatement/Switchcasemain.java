class Switchcasemain{
    public static void main(String[] args)
    {
        int no = 5;
        switch(no)
        {
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("Three");
                break;
            default:
                System.out.println("You press 1,2,3 only");
        }
    }
}